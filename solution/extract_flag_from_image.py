def binary_to_text(binary):
    chars = [binary[i:i+8] for i in range(0, len(binary), 8)]
    return ''.join(chr(int(char, 2)) for char in chars if char != '00000000')

def extract_text_from_binary_image(image_binary):
    """Extracts hidden text from the binary image using LSB steganography."""
    # Flattened the image binary into a single string
    image_binary_flat = ''.join(image_binary.split())

    # Extracted the least significant bits
    lsb_bits = ''.join(pixel[-1] for pixel in image_binary_flat)

    # Splited the binary string into 8-bit chunks and converted to text
    extracted_text = binary_to_text(lsb_bits)

    return extracted_text

